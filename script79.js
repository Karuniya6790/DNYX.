
let a = 50;
let b = "50";

console.log(a == b);  //loose equality

// Strict equality 
console.log(a === b); 
/*
 Explanation:
 * - Loose equality (==) compares values for equality after converting both values to a common type.
 * - Strict equality (===) compares both the value and the type, without performing type conversion.
 */
